    public static void fillStudentJtable () {
        ContactQuery cq = new ContactQuery();
        ArrayList<contactPrivate> cList = cq.contactList();
        
        String[] colNames = {"Id","Nume", "Prenume", "DataNasterii", "Sex", "NumarDeTelefon", "TipTelefon" };
        Object[][] rows = new Object[cList.size()][7];
        
        for(int i=0; i<cList.size(); i++) {
                rows[i][0] = cList.get(i).getCid();
                rows[i][1] = cList.get(i).getFnume();
                rows[i][2] = cList.get(i).getLnume();
                rows[i][3] = cList.get(i).getBdate();
                rows[i][4] = cList.get(i).getSex();
                rows[i][5] = cList.get(i).getPhone();
                rows[i][6] = cList.get(i).getTipTel();
        }
           
        
        MyModel mmd = new MyModel(rows,colNames);
        AdminContacte.jTable1.setModel(mmd);
    }
    
    public boolean verifNumar() {
        Pattern p = Pattern.compile("^[0-7][0-9]{9}$");
        Matcher m = p.matcher(jTextField_NrTf.getText());
        if(!m.matches()) {
           JOptionPane.showMessageDialog(null, "Numar de telefon Invalid");
           return false;
        } else {
        return true;
        }
    }
        public boolean verifNume() {
        Pattern p = Pattern.compile("[A-Za-z]{2,20}");
        Matcher m = p.matcher(jTextField_Nume.getText());
        if(!m.matches()) {
           JOptionPane.showMessageDialog(null, "Numele trebuie sa inceapa cu litera mare si sa fie fromat din mai mult de 2 litere");
           return false;
        } else {
        return true;
        }
    }
    public boolean verifText(){
        
        if(jTextField_Nume.getText().equals("") || jTextField_Prenume.getText().equals("") 
               || jTextField_NrTf.getText().equals("") || jDateChooser.getDate() == null)
        {
           JOptionPane.showMessageDialog(null, "Unul sau mai multe campuri sunt goale");
            return false;
        }
        
        else if(isSameDay(jDateChooser.getDate(),new Date())){
            JOptionPane.showMessageDialog(null, "Astazi este ziua dvs. La multi ani!!!");
            return true;
        }
        else if(jDateChooser.getDate().compareTo(new Date()) > 0){
            JOptionPane.showMessageDialog(null, "Introduce o data de nastere corecta");
            return false;
        }
        else {
            return true;
        }
    }
    
     private boolean isSameDay(Date date1, Date date2) {
    Calendar calendar1 = Calendar.getInstance();
    calendar1.setTime(jDateChooser.getDate());
    Calendar calendar2 = Calendar.getInstance();
    calendar2.setTime(date2);
    boolean sameYear = calendar1.get(Calendar.YEAR) == calendar2.get(Calendar.YEAR);
    boolean sameMonth = calendar1.get(Calendar.MONTH) == calendar2.get(Calendar.MONTH);
    boolean sameDay = calendar1.get(Calendar.DAY_OF_MONTH) == calendar2.get(Calendar.DAY_OF_MONTH);
    return sameDay && sameMonth;
    }
    
