    public void refreshTable() {
        AdminContacte.jTable1.setModel(new DefaultTableModel(null, new Object[]{"Id", "Nume", "Prenume", "Sex", "DataNasterii", "Telefon", "TipTelefon"}));
        fillStudentJtable();
    }
    private void jButtonAdauga1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdauga1ActionPerformed
        String fnume = jTextField_Nume.getText();
        String lnume = jTextField_Prenume.getText();
        String phone = jTextField_NrTf.getText();
        String tipTel = "Fix";
        if (jRadioButtonMobil.isSelected()) {
            tipTel = "Mobil";
        }
        String sex = "Masculin";
        if (jRadioButtonFeminin.isSelected()) {
            sex = "Feminin";
        }
        
        
        
        if(verifText() && verifNumar() && verifNume() && verifDacaExista()){
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyy-MM-dd");
        String bdate = dateFormat.format(jDateChooser.getDate());
        contactPrivate cnt = new contactPrivate(null, fnume, lnume, bdate, sex, phone, tipTel);
        ContactQuery cq = new ContactQuery();
        cq.insertContact(cnt);
        refreshTable();
        }
    }//GEN-LAST:event_jButtonAdauga1ActionPerformed

