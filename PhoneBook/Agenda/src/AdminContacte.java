    public boolean verifText(){
        if(jTextField_Nume.getText().equals("") || jTextField_Prenume.getText().equals("")
                || jTextField_Id.getText().equals("") || jTextField_NrTf.getText().equals("") || jDateChooser.getDate() == null)
        {
           JOptionPane.showMessageDialog(null, "Unul sau mai multe campuri sunt goale");
            return false;
        }
        else if(isSameDay(jDateChooser.getDate(),new Date())){
            JOptionPane.showMessageDialog(null, "Astazi este ziua dvs. La multi ani!!!");
            return true;
        }
        else if(jDateChooser.getDate().compareTo(new Date()) > 0){
            JOptionPane.showMessageDialog(null, "Data nasterii nu este valida!");
            return false;
        }
        return true;
    }
    
    private boolean isSameDay(Date date1, Date date2) {
    Calendar calendar1 = Calendar.getInstance();
    calendar1.setTime(jDateChooser.getDate());
    Calendar calendar2 = Calendar.getInstance();
    calendar2.setTime(date2);
    boolean sameYear = calendar1.get(Calendar.YEAR) == calendar2.get(Calendar.YEAR);
    boolean sameMonth = calendar1.get(Calendar.MONTH) == calendar2.get(Calendar.MONTH);
    boolean sameDay = calendar1.get(Calendar.DAY_OF_MONTH) == calendar2.get(Calendar.DAY_OF_MONTH);
    return sameDay && sameMonth;
    }
