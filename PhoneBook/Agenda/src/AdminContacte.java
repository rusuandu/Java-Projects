    public boolean verifNume() {
        Pattern p = Pattern.compile("[A-Za-z]{2,20}");
        Matcher m = p.matcher(jTextField_Nume.getText());
        if(!m.matches()) {
           JOptionPane.showMessageDialog(null, "Numele trebuie sa inceapa cu litera mare si sa fie fromat din mai mult de 2 litere");
           return false;
        } else {
        return true;
        }
    }
    public boolean verifNumar() {
        Pattern p = Pattern.compile("^[0-7][0-9]{9}$");
        Matcher m = p.matcher(jTextField_NrTf.getText());
        if(!m.matches()) {
           JOptionPane.showMessageDialog(null, "Numar de telefon Invalid");
           return false;
        } else {
        return true;
        }
    }
    
