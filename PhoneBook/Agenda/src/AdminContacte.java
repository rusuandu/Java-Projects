    private void jMenuItemSalvareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemSalvareActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Salvare tabel");
        chooser.showSaveDialog(null);
        File f = chooser.getSelectedFile();
        String filePath = f.toString();
        File file = new File(filePath);
        
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bf = new BufferedWriter(fw);
            
            for(int i=0; i<jTable1.getRowCount(); i++) {
                for(int j=0; j<jTable1.getColumnCount(); j++) {
                    bf.write(jTable1.getValueAt(i, j).toString()+" ");
                }
                bf.newLine();
            }
            bf.close();
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(AdminContacte.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jMenuItemSalvareActionPerformed

