        // TODO add your handling code here:
        int rowIndex = jTable1.getSelectedRow();
        
        if(jTable1.getValueAt(rowIndex, 4).toString().equals("Masculin")) 
        {
            jRadioButtonMasculin.setSelected(true);
            jRadioButtonFeminin.setSelected(false);
        } else {
            jRadioButtonFeminin.setSelected(true);
            jRadioButtonMasculin.setSelected(false);
        }
        
        if(jTable1.getValueAt(rowIndex, 6).toString().equals("Mobil")) 
        {
            jRadioButtonMobil.setSelected(true);
            jRadioButtonFix.setSelected(false);
        } else {
            jRadioButtonFix.setSelected(true);
            jRadioButtonMobil.setSelected(false);
        }
        
        jTextField_Id.setText(jTable1.getValueAt(rowIndex, 0).toString());
        jTextField_Nume.setText(jTable1.getValueAt(rowIndex, 1).toString());
        jTextField_Prenume.setText(jTable1.getValueAt(rowIndex, 2).toString());
        jTextField_NrTf.setText(jTable1.getValueAt(rowIndex, 5).toString());
        
        Date bdate;
        try {
            bdate = new SimpleDateFormat("yyy-MM-dd").parse(jTable1.getValueAt(rowIndex, 3).toString());
            jDateChooser.setDate(bdate);
        } catch (ParseException ex) {
            Logger.getLogger(AdminContacte.class.getName()).log(Level.SEVERE, null, ex);
        }
